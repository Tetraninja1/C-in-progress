#include <iostream>
using namespace std;

// operators in cpp

int main(){
    int a,b;
    cout<<"enter the value of a and b"<<endl;
    cin>>a>>b;
    cout<<"The value of a + b"<<endl<<(a+b)<<endl;
    cout<<"The value of a - b"<<endl<<(a-b)<<endl;
    cout<<"The value of a * b"<<endl<<(a*b)<<endl;
    cout<<"The value of a / b"<<endl<<(a/b)<<endl;
    cout<<"The value of a % b"<<endl<<(a%b)<<endl;
    cout<<"The value of a  b"<<endl<<(a+b)<<endl;
    cout<<"The value of a++ "<<endl<<(a++)<<endl;
    cout<<"The value of ++a"<<endl<<(++a)<<endl;
    cout<<"The value of a--"<<endl<<(a--)<<endl;
    cout<<"The value of --a"<<endl<<(--a)<<endl;
    return 0;
}